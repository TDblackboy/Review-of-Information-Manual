package modal;

public class TypeSet {
	private int id;
	private String reci;
	private String type;
	private String keyWord;
	private String mohu;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getReci() {
		return reci;
	}
	public void setReci(String reci) {
		this.reci = reci;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getKeyWord() {
		return keyWord;
	}
	public void setKeyWord(String keyWord) {
		this.keyWord = keyWord;
	}
	public String getMohu() {
		return mohu;
	}
	public void setMohu(String mohu) {
		this.mohu = mohu;
	}
	
}

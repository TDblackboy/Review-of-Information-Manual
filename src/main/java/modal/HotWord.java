public class HotWord {
	private String word;
	private String explaining;
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getExplaining() {
		return explaining;
	}
	public void setExplaining(String explaining) {
		this.explaining = explaining;
	}
	
}

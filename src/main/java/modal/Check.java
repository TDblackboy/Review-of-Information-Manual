package modal;

public class Check {
	private int id;
	private String word;
	private String degree;
	private String dbText;
	private String netText;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getDbText() {
		return dbText;
	}
	public void setDbText(String dbText) {
		this.dbText = dbText;
	}
	public String getNetText() {
		return netText;
	}
	public void setNetText(String netText) {
		this.netText = netText;
	}
	
}
